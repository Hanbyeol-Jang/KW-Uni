Mmul_data
	STMEA sp!,{r0,r4-r12}	; 바뀌면 안되는 register 값들을 stack에 쌓음
	
	MOV r9, #0
	MOV r12, #16
;;;;;;;;;;;;;;;;;;NaN 인지 체크	;;;;;;;;;;;;;;;;;;;;;;;;
check_v1
	MOV r3, r1				; V1 이 NaN 인지 체크
	MOV r3, r3, LSL#1		; exponent 추출
	MOV r3, r3, LSR#24
	CMP r3, #255			; r3=11111111 ? 무한대?
	BEQ check_f1
	BNE check_v2

check_f1
	MOV r4, r1 			; fraction 추출
	MOV r4, r4, LSL#9
	MOV r4, r4, LSR#9
	CMP r4, #0			; 0 이 아니면 NaN
	MOVNE r3, r1
	BNE	  Result_m
	
check_v2
	MOV	r3, r2				; V2 가 NaN 인지 체크
	MOV r3, r3, LSL#1
	MOV r3, r3, LSR#24		; exponent 추출
	CMP r3, #255			; 무한대?
	BEQ check_f2
	BNE check_z
	
check_f2
	MOV r4, r2			
	MOV r4, r4, LSL#9		; fraction 추출
	MOV r4, r4, LSR#9
	CMP r4, #0			; 0 이 아니면 NaN
	MOVNE r3, r2
	BNE	  Result_m
;;;;;;;;;;;;;;;;;;0 인지 체크;;;;;;;;;;;;;;;;;;;;;;;;;;
check_z
	MOV r3, r1				; V1 이 0인지 체크
	MOV r3, r3, LSL#1		; exponent & fraction 추출
	MOVS r3, r3, LSR#1
	MOVEQ r3, r1			; result = V1
	BEQ Result_m
	
	MOV r3, r2				; v2 가 0인지 체크
	MOV r3, r3, LSL#1		; exponent & fraction 추출
	MOVS r3, r3, LSR#1
	MOVEQ r3,r2				; result = V2
	BEQ Result_m
;;;;;;;;;;;;;Sign bit, Exponent, Mantissa 저장;;;;;;;;;;;;;;;;;	
	MOV r3, r1, LSR#31		; 1값 sign bit 따로 저장
	MOV r4, r2, LSR#31		; 2값 sign bit 따로 저장
	EOR r3, r3, r4			; r3 = result 의 sign bit
	MOV r3, r3, LSL#31		; S 자리 맞춰줌
	
	MOV r5, r1, LSL#1		; 1값 exponent 따로 저장
	MOV r5, r5, LSR#24
	CMP r5, #255			; E1 = 11111111, 무한대?
	MOVEQ r5, r5,LSL#23
	BEQ Result_m
	
	MOV r6, r2, LSL#1		; 2값 exponent 따로 저장
	MOV r6, r6, LSR#24
	CMP r6, #255			; E2 = 11111111, 무한대?
	MOVEQ r5, r6,LSL#23
	BEQ Result_m
	
	ADD r5, r5, r6			; E1 + E2 - 127, r5 에 저장
	SUB r5, r5, #127
	MOV r5, r5, LSL#24		; E 자리 맞춰줌
	MOV r5, r5, LSR#1
	
	MOV r7, r1, LSL#9		; 1값 mantissa 따로 저장
	MOV r7, r7, LSR#2
	ADD r7, r7, #0x40000000	; 1.mantissa 꼴 만들어줌
	MOV r8, r2, LSL#9		; 2값 mantissa 따로 저장
	MOV r8, r8, LSR#17
	ADD r8, r8, #0x8000		; 1.mantissa 꼴 만들어줌
	
	BAL booth_m

booth_m
	CMP r12, #0				; 연산 다 하면 나옴
	MOVEQ r9, r9, LSR#5		; r9 = result 의 mantissa
	BEQ adjust_e
	
	MOV r4, r8, LSL#30		; mantissa 의 맨 뒤 2bit 확인
	MOV r4, r4, LSR#30
	
	CMP r4,#2_00			; 00 일 때
	MOVEQ r9, r9, ASR#1		; shift
	MOVEQ r8, r8, LSR#1
	SUBEQ r12, r12, #1
	BEQ booth_m
	
	CMP r4,#2_01			; 01 일 때
	ADDEQ r9, r9, r7			; ADD 후
	MOVEQ r9, r9, ASR#1		; shift
	MOVEQ r8, r8, LSR#1
	SUBEQ r12, r12, #1
	BEQ booth_m
	
	CMP r4,#2_10			; 10 일 때
	RSBEQ r6, r7,#0			; 보수 취해줌
	ADDEQ r9, r9, r6		; -A를 더함 : SUB
	MOVEQ r9, r9, ASR#1		; shift
	MOVEQ r8, r8, LSR#1
	SUBEQ r12, r12, #1
	BEQ booth_m

	CMP r4,#2_11			; 11 일 때
	MOVEQ r9, r9, ASR#1		; shift
	MOVEQ r8, r8, LSR#1
	SUBEQ r12, r12, #1
	BEQ booth_m
	
adjust_e
	MOV r4, r9, LSR#23			; adjust exponent
	
	CMP r4,#2_00				; when 
	SUBEQ r5, r5, #0x800000		; result 
	MOVEQ r9, r9, LSL#9			; 00
	MOVEQ r9, r9, LSR#9
	BEQ Result_m
	
	CMP r4,#2_01				; when
	MOVEQ r9, r9, LSL#9			; result
	MOVEQ r9, r9, LSR#9			; 01
	BEQ Result_m
	
	CMP r4,#2_10				; when
	ADDEQ r5, r5, #0x800000		; result
	MOVEQ r9, r9, LSL#9			; 10
	MOVEQ r9, r9, LSR#10
	BEQ Result_m
	
	CMP r4,#2_11				; when
	ADDEQ r5, r5, #0x800000		; result
	MOVEQ r9, r9, LSL#9			;10
	MOVEQ r9, r9, LSR#10
	ADDEQ r9, r9, #0x400000
	BEQ Result_m
	
Result_m
	ADD r3, r3, r5			; result 값 합침
	ADD r3, r3, r9			; Result = S + E + M
	
	MOV r0, r3, LSL#1		; 결과 값이
	MOV r0, r0, LSR#24		; 무한대 일 때 확인 해주어야함
	CMP r0, #0xFF			; exponent 가 11111111 이면
	BNE Madd_data
	BEQ check_man
	
check_man
	MOV r0, r3, LSL#9		; mantissa 확인
	MOV r0, r0, LSR#9
	CMP r0,#0				; 0 이면
	BEQ check_inf			; 무한대
	BNE check_nan			; 아니면 NaN
check_inf
	MOV r0, r3, LSR#31
	CMP r0, #0
	ADDEQ r8, r8, #1			; 양의 무한대
	SUBNE r8, r8, #1			; 음의 무한대
	B Madd_data
	
check_nan
	MOVNE r9, r3			; NaN
	BNE Loop2
	
;;;;;;;;;;;;;;;;;;;;;;; floating point ADD;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
Madd_data
	LDMEA sp!, {r0,r4-r12}
	STMEA sp!,{r0,r3-r8,r10-r12}
	MOV r1, r9				; Value 1 불러오기
	MOV r2, r3				; Value 2 불러오기
;;;;;;;;;;;;;;;;;;;;;;;;0인지 체크;;;;;;;;;;;;;;;;;;;;;
	MOV r3, r1				; r1 이 0인지 아닌지 확인
	MOV r3, r3,LSL#1		; Exponent & fraction 추출
	MOVS r3, r3,LSR#1		; 0 이면
	MOVEQ r9, r2			; r2 값 저장
	BEQ	result
	
	MOV r3, r2				; r2 가 0인지 아닌지 확인
	MOV r3, r3, LSL#1		; Exponent & fraction 추출
	MOVS r3, r3, LSR#1		; 0 이면
	MOVEQ r9, r1			; r1 값 저장
	BEQ result
;;;;;;;;;;;;;;;;;Sign bit,Exponent,Mantissa 추출;;;;;;;;;;;;;;;;
	MOV r3, r1, LSR#31		; 1값 sign bit 따로 저장
	MOV r4, r2, LSR#31		; 2값 sign bit 따로 저장
	
	MOV r5, r1, LSL#1		; 1값 exponent 따로 저장
	MOV r5, r5, LSR#24
	MOV r6, r2, LSL#1		; 2값 exponent 따로 저장
	MOV r6, r6, LSR#24
	
	MOV r7, r1, LSL#9		; 1값 mantissa 따로 저장
	MOV r7, r7, LSR#9
	ADD r7, r7, #0x800000	; 1.mantissa 꼴 만들어줌
	MOV r8, r2, LSL#9		; 2값 mantissa 따로 저장
	MOV r8, r8, LSR#9
	ADD r8, r8, #0x800000	; 1.mantissa 꼴 만들어줌
	
	CMP r5, r6				; 3. compare exponents
	SUBGT r10, r5, r6		; r9 : r5 가 r6 보다 얼마나 더 큰지
	MOVGT r8, r8, LSR r10	; 4. shift smaller mantissa
	MOVGT r11, r5			; r11 : result E
	
	SUBLE r10, r6, r5		; r9 : r6가 r5 보다 얼마나 더 큰지
	MOVLE r7, r7, LSR r10
	MOVLE r11, r6			; r11 : result E
	
	CMP r3,r4				; sign bit 비교
	BEQ	S_same				; sign bit 이 같을 때
	BNE	S_different				; sign bit 이 다를 때
	
S_same
	ADD r12, r7, r8			; add mantissa
	
	MOV r0, #0x1000000		; check carry
	AND r10, r12, r0
	
	CMP r10, r0				; when carry
	MOVEQ r12, r12, LSR#1	; normalize mantissa
	ADDEQ r11, r11,#1		; result E +1 (adjust exponent)
	
	SUB r12, r12, #0x800000	; mantissa -> fraction
	MOV r11, r11, LSL#23	; result E
	MOV r3, r3, LSL#31		; result S
	ADD r9, r3, r11		; combine result S,E,M
	ADD r9, r9, r12
	B	result
	
S_different
	CMP r5, r6				; sign bit 비교
	SUBGT r12, r7, r8		; mantissa 빼줌
	MOVGT r3, r3
	
	SUBLT r12, r8, r7		; mantissa 빼줌
	MOVLT r3, r4
	
	MOVEQ lr, pc
	BEQ E_same
	MOV r0, #0

Loop
	AND r10, r12, #0x800000			; 유효숫자 체크
	CMP r10, #0x800000
	MOVNE r12, r12,LSL#1			; 한자리씩 옮겨가며 확인
	ADDNE r0, r0, #1 
	BNE Loop
	
	SUB r11,r11,r0
	
	SUB r12, r12, #0x800000			; fraction 설정
	MOV r11, r11, LSL#23
	MOV r3, r3, LSL#31
	ADD r9, r3, r11
	ADD r9, r9, r12
	B	result
	
E_same
	CMP r7,r8
	SUBGT r12, r7, r8		; M1 > M2
	MOVGT r3, r3			; result S = S1
	
	SUBLT r12, r8, r7		; M1 < M2
	MOVLT r3, r4			; result S = S2
	
	MOVEQ r9,#0			; E same, M same -> 0
	BEQ	result
	MOV pc, lr
	
result
	LDMEA sp!,{r0,r3-r8,r10-r12}
	
	CMP r8, #0					; 무한대 count 체크
	BNE check_inf2
	BEQ check_zz
	
check_inf2
	CMP r8, #0
	MOVGT r9,#0xff		  ; 무한대
	MOVGT r9, r9, LSL#23
	MOVLT r9,#0xff
	MOVLT r9, r9, LSL#23  ; -무한대
	ADDLT r9,r9,#0x80000000
	B Addition2
	
check_zz
	MOV r1, r1, LSL#1
	MOV r1, r1, LSR#24
	CMP r1, #0xff
	MOVEQ r9, #0				; 양의 무한대와 음의 무한대 갯수가 같으니 0
	BEQ Addition2
	B Addition2					; 무한대가 아니면 그냥 더함
	
	MOV r2, r2, LSL#1
	MOV r2, r2, LSR#24
	CMP r2, #0xff
	MOVEQ r9, #0				; 양의 무한대와 음의 무한대 갯수가 같으니 0
	B Addition2					; 무한대가 아니면 그냥 더함
	
	
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
	
Addition2
	ADD r0, r0, #1					; C 의 원소 하나 카운트
	CMP r0, #30						; C 의 원소 하나 완성시
	BEQ Loop2						; B 다음 세로줄
	B Loop1
	
Loop2
	STR r9,[r7],#4
	SUB r4, r4, #0x78
	;MUL r6, r11, r5;;;;;;;;;;;;;;;;;;;
	;ADD r4, r4,r6
	
	SUB r12, r12,#0xE10
									; Value2 initialization, B 다음 세로 줄 위해

	ADD r12,r12,#4				; 다음 세로 줄로 건너뜀
	MOV r8, #0
	MOV r9, #0						; 원소 하나 완성했으니 initialization
	MOV r0, #0
	ADD r10, r10, #1				; B의 끝 세로줄 까지 갔는지 확인
	CMP r10, #31					; B의 끝까지 갔으면
	BEQ Loop3						; A의 다음 가로줄, B 처음 세로줄로 바꿔줘야함
	B	Loop1
	
Loop3
	ADD r11, r11, #1					; Value1 initialization, A 다음 가로 줄 위해
	ADD r4, r4, r5					; A 다음 가로줄로 건너뜀
	;ADD r4,r4,r6
						; Value2 initialization,
	SUB r12, r12, #0x78
	MOV r10,#1						; B 처음 세로줄로 바꿔줘야함
	CMP r11, #31						; A 의 가로줄 끝까지 갔으면
	BEQ Endline						; Endline
	B Loop1							; 아니면 연산 다시 시작
	
Endline
	MOV pc, #0				;Program end
	
