grade=int(input("점수 입력:"))

if grade>=95 and grade<=100:
    print("A+")
elif grade >=90 and grade <95:
    print("A")
elif grade >=85 and grade <90:
    print("B+")
elif grade >=80 and grade <85:
    print("B")
elif grade >=75 and grade <80:
    print("C+")
elif grade >=70 and grade <75:
    print("C")
else:
    print("F")
    
